    // AuthToken
    internal class AuthTokenConfiguration : EntityTypeConfiguration<AuthToken>
    {
        public AuthTokenConfiguration()
            : this("auth")
        {
        }
 
        public AuthTokenConfiguration(string schema)
        {
            ToTable(schema + ".AuthToken");
            HasKey(x => x.Id);

            Property(x => x.Id).HasColumnName("Id").IsRequired().HasColumnType("int").HasDatabaseGeneratedOption(DatabaseGeneratedOption.Identity);
            Property(x => x.AuthCode).HasColumnName("AuthCode").IsRequired().HasColumnType("nvarchar");
            Property(x => x.Token).HasColumnName("Token").IsRequired().HasColumnType("nvarchar");
        }
    }

